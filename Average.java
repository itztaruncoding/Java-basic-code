package com.tarun;

public class Average {

	public static void main(String args[]) {
		
		float num1 = 50;
		float num2 = 24;
		float num3 = 42;
		float num4 = 34;
		
		float ave;
		
		ave = (num1 + num2 + num3 + num4) / 4;
		
		System.out.println("Average of above 4 numbers is: " + ave);
		
	}
}
